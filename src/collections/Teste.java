public class Teste {

    public static void main(String[] args) throws Exception {
        // Declara uma variável 'numero1' do tipo int e a inicializa com o valor 1.
        int numero1 = 1;

        // Declara uma variável 'numero2' do tipo String e a inicializa com a string "2".
        String numero2 = "2";

        // Realiza uma operação de concatenação entre 'numero1' (int) e 'numero2' (String).
        // Como 'numero1' é um int e 'numero2' é uma String, a concatenação resulta em uma String.
        // Portanto, "1" (String) + "2" (String) resulta em "12" (String).
        System.out.println(numero1 + numero2); // Imprime "12"
    }
}
