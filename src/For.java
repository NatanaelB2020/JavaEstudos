public class For {
    public static void main(String[] args) {
        System.out.println("sistema..."); // Imprime "sistema..." no console

        // Inicia um loop 'for' que começa com ct = 0, continua enquanto ct for menor ou igual a 10 e incrementa ct em 1 a cada iteração
        for(int ct = 0; ct <= 10; ct++){
            System.out.println("-> executando " + " " + ct); // Imprime "-> executando" seguido do valor atual de ct no console
        }

        System.out.println("final "); // Imprime "final" no console
    }
}
